package com.java.DecisionControlStatements;

public class If_Else {

	public static void main(String[] args) {
		/*
		 * if(condition){
		 *   if block statements
		 * }else{
		 *   else block statements
		 *   } 
		 */
		boolean result = false;
		if(result){
			System.out.println("Hello");
		}else{
			System.out.println("Hi");
		}
		

	}

}
